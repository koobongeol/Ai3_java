package Day01;

public class Ex01_print {
     // 프로그램 실행 : ctrl +F11
     // main : ctrl+ space
	// 프로그램이 시작되는 메소드
	public static void main(String[] args) {
		// 기본 출력 메소드
		// - print(), println() 메소드를 호출하여 출력한다.
		// - System.out 출력 개체를 사용한다.
		// - System : ctrl + space
		System.out.println("안녕하세요");
		
		//println();        - ln (line)
		//: 지정된 문자열을 출력한 후, 한 줄 엔터
		System.out.println("안녕하세요 출력 후 줄바꿈");
		
		
		System.out.println("자바 첫 수업");
		System.out.println();
		System.out.println("이어지는 문자열...");
		System.out.println("하이용");
		System.out.print("테스트");
		System.out.println("ㅇㅇ");
		System.out.println();
	}
}
